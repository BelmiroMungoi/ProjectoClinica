    public void exibirMedico() {
        connect.conexao();
        connect.executaSql("select * from medicos order by nome_medico");
        try {
            connect.rs.first();
            jComboBoxMed.removeAllItems();
            do {                
                jComboBoxMed.addItem(connect.rs.getString("nome_medico"));
            } while (connect.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao exibir medicos"  + ex);
        }
        connect.desconectar();
    }
    
    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID","Nome","Motivo","Data","Turno","Medico","Status"}; 
        connect.conexao();
        connect.executaSql(sql);
        try {
            connect.rs.first();
            do {                
                dados.add(new Object[]{connect.rs.getInt("cod_agenda"),
                    connect.rs.getString("nome_paciente"), connect.rs.getString("agen_motivo"),
                    connect.rs.getString("agen_data"), connect.rs.getString("agen_turno"),
                    connect.rs.getString("nome_medico"),connect.rs.getString("agen_status")});
            } while (connect.rs.next());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Sem Agendamentos para o medico");
        }
        
        ModelTable model = new ModelTable(dados, colunas);
        jTableAgenda.setModel(model);
        jTableAgenda.getColumnModel().getColumn(0).setPreferredWidth(40);
        jTableAgenda.getColumnModel().getColumn(0).setResizable(false);
        jTableAgenda.getColumnModel().getColumn(1).setPreferredWidth(160);
        jTableAgenda.getColumnModel().getColumn(1).setResizable(false);
        jTableAgenda.getColumnModel().getColumn(2).setPreferredWidth(140);
        jTableAgenda.getColumnModel().getColumn(2).setResizable(false);
        jTableAgenda.getColumnModel().getColumn(3).setPreferredWidth(90);
        jTableAgenda.getColumnModel().getColumn(3).setResizable(false);
        jTableAgenda.getColumnModel().getColumn(4).setPreferredWidth(70);
        jTableAgenda.getColumnModel().getColumn(4).setResizable(false);
        jTableAgenda.getColumnModel().getColumn(5).setPreferredWidth(160);
        jTableAgenda.getColumnModel().getColumn(5).setResizable(false);
        jTableAgenda.getColumnModel().getColumn(6).setPreferredWidth(120);
        jTableAgenda.getColumnModel().getColumn(6).setResizable(false);
        jTableAgenda.getTableHeader().setReorderingAllowed(false);
        jTableAgenda.setAutoResizeMode(jTableAgenda.AUTO_RESIZE_OFF);
        jTableAgenda.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        connect.desconectar();
    }

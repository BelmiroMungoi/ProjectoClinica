    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        consulta.setNomePac(jTextFieldNome.getText());
        consulta.setDiaConsulta(jTextFieldDiag.getText());
        consulta.setReceConsulta(jTextFieldRec.getText());
        if(jTextFieldDiag.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo DIAGNOSTICO");
            jTextFieldDiag.requestFocus();
        } else if (jTextFieldRec.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a RECEITA");
            jTextFieldRec.requestFocus();
        }else {
            DaoConsulta dao = new DaoConsulta();
            dao.salvar(consulta);
            jTextFieldDiag.setText("");
            jTextFieldRec.setText("");
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

